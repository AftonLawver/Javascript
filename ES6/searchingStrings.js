const planet = "Earth";
// startsWith method is case sensitive
console.log(planet.startsWith("Ear"));

// endsWith method is case sensitive
console.log(planet.endsWith("th"));

// will return true or false if the entire string that
// was passed in is in the string object that was created above.
console.log(planet.includes("h"))

// returns the index of the first occurrence of the passed in string
console.log(planet.search("art"));

